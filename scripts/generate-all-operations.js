import { promises as fs } from 'fs';
import fetch from 'node-fetch';
import { getIntrospectionQuery, buildClientSchema } from 'graphql';

const ENDPOINT = 'http://localhost:5555/graphql';
const OUT_FILE = 'src/shared/api/gql/all-operations.graphql';

async function main() {
  const res = await fetch(ENDPOINT, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ query: getIntrospectionQuery() }),
  });
  const json = await res.json();
  const schema = buildClientSchema(json.data);

  const queryType = schema.getQueryType();
  const mutationType = schema.getMutationType();

  const queryFields = queryType ? Object.keys(queryType.getFields()) : [];
  const mutationFields = mutationType ? Object.keys(mutationType.getFields()) : [];

  let operations = '';

  if (queryFields.length) {
    operations += `query AutoGeneratedQueries {\n${queryFields
      .map(f => `  ${f}`)
      .join('\n')}\n}\n\n`;
  }

  if (mutationFields.length) {
    operations += `mutation AutoGeneratedMutations {\n${mutationFields
      .map(f => `  ${f}`)
      .join('\n')}\n}\n`;
  }

  await fs.mkdir('src/shared/api/gql', { recursive: true });
  await fs.writeFile(OUT_FILE, operations);
  console.log(`✅ Generated valid operations into ${OUT_FILE}`);
}

main().catch(console.error);
